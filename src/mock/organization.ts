export function P(
  id: any,
  name: any,
  gender: any,
  fac: any,
  dept: any,
  specialty: any,
  specCode: any,
  role: any,
  status: any,
  years: any,
  panel: any,
  contracts: any,
  composite: any,
  proms: any,
  ehr: any,
  board: any,
  langs: any,
  start: any,
) {
  const initials = name
    .split(" ")
    .filter((s: any) => !/^(dr\.?|prof\.?|maj\.?|col\.?|lt\.?)$/i.test(s))
    .slice(0, 2)
    .map((s: any) => s[0])
    .join("")
    .toUpperCase();
  const slug = name
    .toLowerCase()
    .replace(/[^a-z]+/g, ".")
    .replace(/^\.|\.$/g, "");
  return {
    id,
    name,
    initials,
    gender,
    facilityId: fac,
    department: dept,
    specialty,
    specCode,
    role,
    status,
    yearsExperience: years,
    panelSize: panel,
    contracts,
    composite,
    promsRate: proms,
    ehrCompleteness: ehr,
    board,
    languages: langs,
    startDate: start,
    npi: "MOD-LIC-" + id.replace("PRV-", ""),
    email: slug + "@modnet.health",
  };
}

